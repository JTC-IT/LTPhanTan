    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        String[] st = txtArray.getText().split("[;]");
        
        int n = st.length;
        //Khai báo mảng số nguyên
        int[] list = new int[st.length];
        //Đổi mảng chuỗi sang mảng số
        for(int i = 0; i < n; i++){
            list[i]=Integer.parseInt(st[i]);
        }
        
        Tuyen t1 = new Tuyen(1, list);
        Tuyen t2 = new Tuyen(2, list);
        
        t1.start();
        t2.start();
        while (!kt1 && !kt2);
        lblsum1.setText(""+s1);
        lblsum2.setText(""+s2);
        lblsum.setText(""+(s1+s2));
    }//GEN-LAST:event_btnActionPerformed

