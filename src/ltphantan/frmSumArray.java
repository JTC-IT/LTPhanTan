    long s1=0,s2=0;
    boolean kt1=false,kt2=false;
          //Tạo lớp tuyến đoạn chúng
    public class Tuyen extends Thread{
       int td, n; //lưu tuyến đoạn thứ mấy
       int[] a; //Mảng a cần tính tổnge
       public Tuyen(int td, int[] a){ //Tạo hàm tạo
           this.td=td;
           this.a=a;
           n = a.length;
       }
       public void run(){
            if(td==1){//nếu là tuyến 1
               for(int i=0; i<n/2; i++)
                   s1 += a[i];
               kt1=true;
            }
            if(td==2){////nếu là tuyến 2
               for(int i=n/2; i<n; i++)
                   s2 += a[i];
               kt2=true;
           }
        }
    }
