    public class TuyenNhan extends Thread{
        public void run(){
            while(true){
                try {
                    String st = gn.Nhan();
                    if(st != null && !st.isEmpty()){
                        System.out.println(st);
                        list.add(st);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    System.exit(0);
                }
            }
        }
    }
    
    private void Send(String mess){
        try {
            String ip = InetAddress.getLocalHost().getHostAddress();
            gn.Gui(ip+":"+mess, ip, 1261);
        } catch (Exception e) {
            e.printStackTrace();
        }
        txtSend.setText("");
    }

